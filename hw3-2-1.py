x = int(input("What is your grade? "))
if x >= 93:
    print("A")
elif x >= 90:
    print("A-")
elif x >= 87:
    print("B+")
elif x >= 83:
    print("B")
elif x >= 80:
    print("B-")
elif x >= 77:
    print("C+")
elif x >= 73:
    print("C")
elif x >= 70:
    print("C-")
elif x >= 65:
    print("D+")
elif x >= 60:
    print("D")
else:
    print("You Failed")
